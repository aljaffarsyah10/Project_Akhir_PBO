	<h3>Apa Itu Smart Contract?</h3>
	<p>Smart Contract atau Kontrak cerdas adalah kontrak perjanjian antara pembeli dan penjual yang dijalankan sendiri (otomatis) dengan ketentuan perjanjiannya yang langsung ditulis ke dalam baris kode program.</p>
	<p>Kode dan perjanjian yang terkandung di dalamnya ada di seluruh jaringan blockchain terdistribusi dan terdesentralisasi. Kode yang ditulis tersebut mengontrol sendiri eksekusi programnya sehingga transaksi dapat dilacak dan tidak dapat diubah.</p>
	<p>Smart contract mengizinkan transaksi dan perjanjian tepercaya untuk dilakukan di antara pihak-pihak yang berbeda dan anonim tanpa memerlukan otoritas pusat, sistem hukum, atau mekanisme penegakan eksternal.</p>

