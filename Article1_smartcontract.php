	<h4>Cara Kerja Kontrak Cerdas</h4>
	<p>Smart contract pertama kali diusulkan pada tahun 1994 oleh Nick Szabo, seorang ilmuwan komputer Amerika yang menemukan mata uang virtual yang disebut “Bit Gold” pada tahun 1998, sepenuhnya 10 tahun sebelum penemuan bitcoin. Bahkan, Szabo sering diisukan sebagai Satoshi Nakamoto yang sebenarnya, penemu anonim bitcoin, yang telah ia bantah.</p>

	<p>Szabo mendefinisikan smart contract sebagai protokol transaksi terkomputerisasi yang menjalankan persyaratan kontrak. Dia ingin memperluas fungsionalitas metode transaksi elektronik, seperti POS (point of sale), ke ranah digital.</p>

	<p>Dalam makalahnya, Szabo juga mengusulkan pelaksanaan kontrak untuk aset sintetis, seperti derivatif dan obligasi. Szabo menulis: “Sekuritas baru ini dibentuk dengan menggabungkan sekuritas (seperti obligasi) dan derivatif (opsi dan futures) dalam berbagai cara. Struktur pembayaran yang sangat kompleks sekarang dapat dibangun ke dalam kontrak standar dan diperdagangkan dengan biaya transaksi rendah. , karena analisis komputerisasi dari struktur istilah yang kompleks ini.”</p>

	<p>Banyak prediksi Szabo di koran menjadi kenyataan dengan cara mendahului teknologi blockchain. Sebagai contoh, perdagangan derivatif sekarang banyak dilakukan melalui jaringan komputer dengan menggunakan struktur istilah yang kompleks.</p>
	
